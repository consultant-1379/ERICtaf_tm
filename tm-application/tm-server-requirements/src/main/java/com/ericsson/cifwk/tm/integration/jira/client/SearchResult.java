package com.ericsson.cifwk.tm.integration.jira.client;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

import java.util.List;

@JsonIgnoreProperties(ignoreUnknown = true)
public class SearchResult {

    @JsonProperty(value = "startAt")
    private int startAt = 0;

    @JsonProperty(value = "maxResults")
    private int resultsPerPage = 0;

    @JsonProperty(value = "total")
    private int totalResults = 0;

    @JsonProperty(value = "issues")
    private List<JiraIssue> issues = null;

    public int getStartAt() {
        return startAt;
    }

    public void setStartAt(int startAt) {
        this.startAt = startAt;
    }

    public int getResultsPerPage() {
        return resultsPerPage;
    }

    public void setResultsPerPage(int resultsPerPage) {
        this.resultsPerPage = resultsPerPage;
    }

    public int getTotalResults() {
        return totalResults;
    }

    public void setTotalResults(int totalResults) {
        this.totalResults = totalResults;
    }

    public List<JiraIssue> getIssues() {
        return issues;
    }

    public void setIssues(List<JiraIssue> issues) {
        this.issues = issues;
    }

    public int getPageCount() {
        return resultsPerPage == 0 ? 0 : (totalResults + resultsPerPage - 1) / resultsPerPage;
    }
}
